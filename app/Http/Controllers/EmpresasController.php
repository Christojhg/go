    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $empresa=Empresa::find($id);
        return view('backend.empresas.edit',compact('empresa'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $file = $request->file('imagen');
        // return $request->get('imagen');
        //obtenemos el nombre del archivo
        $nombre =  time()."_".$file->getClientOriginalName();
        //indicamos que queremos guardar un nuevo archivo en el disco local
        \Storage::disk('empresas')->put($nombre,  \File::get($file));

        $empresa=Empresa::find($id);
        $empresa->nombre=$request->get('nombre');
        $empresa->descripcion=$request->get('descripcion');
        $empresa->imagen=$nombre;
        $empresa->estado=$request->get('estado');
        $empresa->save();
        return redirect()->route('empresa.index');
    }

    /**
     * Remove the specified resource from storage.
